<h1 class="one">01  Software Developer</h1>

<p class="one">Software developers create software programs that allow users to perform specific tasks on various devices, such as computers or mobile devices. They are 
responsible for the entire development, testing, and maintenance of software.

Software developers must have the technical creativity required to solve problems uniquely. They need to be fluent in the computer languages that are used
to write the code for programs.</p>

<h1 class="one">02  Database Administrator</h1>
<p class="one">Database administrators analyze and evaluate the data needs of users. They develop and improve data resources to store and retrieve critical information.

They need the problemsolving skills of the computer major to correct any malfunctions in databases and to modify systems as the needs of users evolve.</p>

<h1 class="one">03  Computer Hardware Engineer</h1>
<p class="one">Computer hardware engineers are responsible for designing, developing, and testing computer components, such as circuit boards, routers, and memory devices.

Computer hardware engineers need a combination of creativity and technical expertise. They must be avid learners who stay on top of emerging trends in the field to create
hardware that can accommodate the latest programs and applications.</p>

<h1 class="one">04  Computer Systems Analyst</h1>
<p class="one">Computer systems analysts assess an organization's computer systems and recommend changes to hardware and software to enhance the company's efficiency.

Because the job requires regular communication with managers and employees, computer systems analysts need to have strong interpersonal skills. Systems analysts need to 
able to convince staff and management to adopt technology solutions that meet organizational needs.</p>

<h1 class="one">05  Computer Network Architect</h1>
<p class="one">Computer network architects design, implement and maintain networking and data communication systems, including local area networks, wide area networks, extranets, and
intranets. They assess the needs of organizations for data sharing and communications.

In addition, computer network architects evaluate the products and services available in the marketplace. Computer network architects test systems before implementation
and resolve problems as they occur after the setup is in place.</p>

<h1 class="one">06  Web Developer</h1>
<p class="one">Web developers assess the needs of users for information-based resources. They create the technical structure for websites and make sure that web pages are accessible 
and easily downloaded through a variety of browsers and interfaces.

Web developers structure sites to maximize the number of page views and visitors through search engine optimization. They must have the communication ability and 
creativity to make sure the website meets its user's needs.</p>

<h1 class="one">07  Information Security Analyst</h1>
<p class="one">Information security analysts create systems to protect information networks and websites from cyber attacks and other security breaches. Their responsibilities also 
include researching trends in data security to anticipate problems and install systems to prevent issues before they occur.

Security analysts also need strong problem-solving skills to investigate breaches, determine the causes, and modify or repair security systems.</p>

<h1 class="one">08  Computer and Information Research Scientists</h1>
<p class="one">Computer and information research scientists invent and design new approaches to computing technology and find innovative uses for existing technology. They study and 
solve complex problems in computing for business, science, medicine, and other fields.

Computer and information research scientists write algorithms that are used to detect and analyze patterns in very large datasets. Some computer and information research 
scientists create the programs that control robots.</p>

<h1 class="one">09  Computer and Information Systems Managers</h1>
<p class="one">Computer and information systems managers analyze a company's technology needs and oversee the implementation of appropriate data systems. They need to be able to evaluate 
software, hardware, networking, and other technology resources for purchase or development.

Because computer and information systems managers hire, train and supervise staff, interpersonal skills are vital in this role. They must be strong leaders who can communicate 
effectively with their staff.</p>

<h1 class="one">10  IT Project Manager</h1>
<p class="one">Project managers in the IT sector coordinate the efforts of a team of programmers and analysts to complete projects. They also analyze technical problems for their company or a 
client organization, proposing solutions and tips to enhance productivity.

Problem-solving skills and a broad knowledge of technology and computer systems help computer science majorsexcel in this role. Strong communication skills are required to 
decipher the needs of users and convey technical specifications to developers.</p></pre>
